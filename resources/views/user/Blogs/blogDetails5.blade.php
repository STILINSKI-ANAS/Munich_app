                    <p>Être étudiant, c'est souvent être à court d'argent. Les sorties nocturnes au Späti de Berlin ou l'entrée au Berghain ne se paient pas toutes seules. C'est pourquoi l'Allemagne est considérée comme l'un des pays les plus propices pour
                        les étudiants marocains qui souhaitent combiner études et travail. En Allemagne, de grandes entreprises proposent des emplois attrayants pour les étudiants ( marocains) et même les universités offrent de nombreux emplois pour les
                        étudiants en parallèle de leurs études. Cependant, travailler en tant qu'étudiant en Allemagne est soumis à certaines règles. Nous vous montrerons comment combiner travail et études en Allemagne et nous vous expliquerons les heures
                        de travail applicables aux étudiants, le plafond de salaire et comment travailler avec un visa étudiant.</p>

