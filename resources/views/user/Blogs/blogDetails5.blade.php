                    <h4>Êtes-vous autorisé à travailler et étudier en Allemagne en tant qu'létudiant marocain ?</h4>

                    <p>En général, en tant qu'étudiant international, vous êtes libre d'occuper un emploi étudiant en Allemagne. Mais il y a une grande condition : en Allemagne, il est fortement recommandé aux étudiants de se consacrer en priorité à leurs
                        études, de sorte qu'un emploi à temps partiel reste réellement un emploi à temps partiel seulement. Pour cette raison, un étudiant travailleur en Allemagne est soumis à certaines règles ; par exemple, un plafond est fixé au nombre
                        d'heures qu'un étudiant travailleur peut passer au travail, et il existe également un plafond pour le salaire que les étudiants peuvent gagner. Voyons cela de plus près !</p>



                    <h4>Combien d'heures de travail les étudiants marocains sont-ils autorisés à effectuer ?

                    </h4>

                    <p> Les étudiants marocains comme les étudiants nationaux, ont le droit de travailler jusqu'à 20 heures par semaine en plus de leurs études. De plus, cette limite est levée pendant les vacances universitaires, vous permettant ainsi de
                        travailler à temps plein en tant qu'étudiant travailleur ! Le nombre d'heures de travail que vous êtes effectivement autorisé à effectuer par semaine en plus de vos études varie entre les étudiants de l'UE/EEE et les étudiants
                        venant de pays en dehors de l'UE.vous êtes libre de travailler en plus de vos études avec votre visa étudiant ! Dans ce cas, les réglementations diffèrent légèrement des règles applicables aux étudiants de l'UE, car les étudiants
                        non-UE/EEE ayant un visa étudiant peuvent travailler jusqu'à 120 jours ouvrables ou 240 demi-journées par an. Vous vous demandez ce qu'est une journée complète et une demi-journée ? Une journée complète signifie 8 heures par jour,
                        soit 40 heures par semaine ; une semaine à temps partiel représente moins de 20 heures par semaine.<br>Vous venez en Allemagne pour suivre un cours de langue ou de préparation ? Malheureusement, les règles sont un peu plus restrictives
                        dans ce cas. Vous n'êtes autorisé à travailler que pendant les vacances et si vous avez reçu un certificat du service de l'enregistrement des étrangers et de l'agence pour l'emploi.</p>

